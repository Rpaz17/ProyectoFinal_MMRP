    public void initBotones() {
        tableroGUI.setLayout(new GridLayout(10, 10));
        int botonsCount = 0;
        for (int f = 0; f < 10; f++) {
            for (int c = 0; c < 10; c++) {
                botons[f][c] = new Botons(f, c, botonsCount, "_", "");
                botons[f][c].setName("boton_" + String.valueOf(botonsCount));
                botons[f][c].setBorder(null);
                botonsCount++;
                botons[f][c].addMouseListener(new MouseAdapter() {
                    public void mouseClicked(MouseEvent evt) {
                        botonesMouseClicked(evt);
                    }
                });
                tableroGUI.add(botons[f][c]);
            }
        }
    }

